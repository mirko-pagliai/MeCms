<h2>Cosa sono i cookie?</h2>
<p>
    I cookie sono piccoli file di testo memorizzati sul vostro computer o
    dispositivo durante la navigazione in internet. I cookie possono essere
    utilizzati per raccogliere, archiviare e condividere frammenti di
    informazioni sulle vostre attività attraverso siti web, incluso il nostro
    sito. Inoltre permettono di ricordare alcuni dettagli circa la vostra
    visita sul nostro sito, come ad esempio la lingua preferita e le altre
    scelte o impostazioni e in generale rendere il sito più facile per voi da
    usare.
</p>
<p>
    Utilizziamo cookie di sessione e cookie persistenti. Un cookie di sessione
    viene utilizzato per identificare una particolare visita al nostro sito.
    Questi cookie scadono dopo un breve periodo di tempo o quando si chiude il
    browser web dopo aver utilizzato i nostri servizi. Utilizziamo questi
    cookie per identificare l'utente durante una singola sessione di
    navigazione, ad esempio quando si accede al nostro sito. Un cookie
    persistente rimane sui vostri dispositivi per un determinato periodo di
    tempo specificato nel cookie. Utilizziamo questi cookie quando abbiamo
    bisogno di identificare l'utente per un periodo di tempo più lungo. Ad
    esempio, potremmo usare un cookie persistente se hai chiesto di
    memorizzare i dati del login
</p>

